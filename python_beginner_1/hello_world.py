# Alles hinter einem '#' wird von python ignoriert, so können wir Kommentare schreiben
#
# Beim Lernen von Programmiersprachen ist es eine weit verbreitete Tradition, 
# als Erstes ein Programm zu schreiben, welches einfach nur `hello world` ausgibt. 
# Diese Tradition wollen wir natürlich nicht brechen, außer dass wir es auf die Hamburger art machen.

print("Moin world")

# Print ist eine Funktion, es wird also ein maschinenbefehl ausgeführt der irgendwas macht
# Funktionen werden wir später genauer kennen lernen, und wir werden auch eigene Funktionen schreiben.
#
# print ist eine sogenannte Built-In Funktion,
# d.h. sie ist ein fester Bestandteil der Programmiersprache Python
# und ihr könnt jederzeit auf sie zugreifen.
# Sie gibt das Funktionsargument auf der Konsole ("Shell" in Thonny) aus.

# "Moin world" ist mit Gänsefüßchen abgetrennt.
# Das bedeutet, dass "Moin world" Daten in Form einer Zeichenkette sind.
# Daten können als Literale (wie in diesem Fall) vorliegen
# Oder sie können aus Funktionsaufrufen erhalten werden (dazu später mehr)
# Es gibt auch Daten in Form von Zahlen, die Literale dazu sehen dann so aus

print(1)   # Das ist eine Ganzzahl, ein sog. Integer
print(1.2) # Das ist eine Kommazahl, ein sog. Float

# Komplexere Datentypen lernen wir später kennen